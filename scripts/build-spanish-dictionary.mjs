import { mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import es from 'dictionary-es';

const ROOT_DIR = process.cwd();
const PUBLIC_OUTPUT = path.join(ROOT_DIR, 'public', 'dictionaries', 'es-words.json');
const SQL_OUTPUT = path.join(ROOT_DIR, 'output', 'dictionary_words.seed.sql');
const MIN_LENGTH = 2;
const MAX_LENGTH = 9;
const SOURCE = 'dictionary-es';
const LICENSE = 'GPL-3.0 OR LGPL-3.0 OR MPL-1.1';

function normalizeWord(word) {
  return word
    .normalize('NFD')
    .replace(/\p{Diacritic}/gu, '')
    .replace(/Ñ/g, 'N')
    .replace(/ñ/g, 'n')
    .replace(/[^a-zA-Z]/g, '')
    .toUpperCase();
}

function parseAffixRules(affText) {
  const rules = new Map();

  for (const line of affText.split(/\r?\n/)) {
    const parts = line.trim().split(/\s+/);
    if ((parts[0] !== 'SFX' && parts[0] !== 'PFX') || parts.length < 5) continue;

    const [type, flag, strip, add, condition] = parts;
    if (strip === 'Y' || strip === 'N') continue;

    const [addText, continuation = ''] = add.split('/');
    const cleanAdd = addText === '0' ? '' : addText;
    const rule = {
      type,
      strip: strip === '0' ? '' : strip,
      add: cleanAdd,
      continuationFlags: [...continuation],
      condition,
    };

    if (!rules.has(flag)) rules.set(flag, []);
    rules.get(flag).push(rule);
  }

  return rules;
}

function parseDictionaryEntry(line) {
  const [word, flags = ''] = line.split('/');
  return { word, flags: [...flags] };
}

function matchesCondition(word, rule) {
  if (rule.condition === '.') return true;
  const pattern = rule.type === 'SFX' ? `${rule.condition}$` : `^${rule.condition}`;
  return new RegExp(pattern, 'u').test(word);
}

function applyAffixRule(word, rule) {
  if (!matchesCondition(word, rule)) return null;

  if (rule.type === 'SFX') {
    if (rule.strip && !word.endsWith(rule.strip)) return null;
    return `${word.slice(0, word.length - rule.strip.length)}${rule.add}`;
  }

  if (rule.strip && !word.startsWith(rule.strip)) return null;
  return `${rule.add}${word.slice(rule.strip.length)}`;
}

function addCandidate(words, displayWords, word) {
  const normalized = normalizeWord(word);
  if (normalized.length < MIN_LENGTH || normalized.length > MAX_LENGTH) return;
  if (!/^[A-Z]+$/.test(normalized)) return;

  words.add(normalized);
  if (!displayWords.has(normalized)) displayWords.set(normalized, word.toLowerCase());
}

function buildDictionary() {
  const rules = parseAffixRules(es.aff.toString('utf8'));
  const words = new Set();
  const displayWords = new Map();
  const entries = es.dic.toString('utf8').split(/\r?\n/).slice(1).filter(Boolean);

  function expandFromFlags(word, flags, depth = 0, seen = new Set()) {
    if (depth > 2) return;

    for (const flag of flags) {
      for (const rule of rules.get(flag) ?? []) {
        const expandedWord = applyAffixRule(word, rule);
        if (!expandedWord || seen.has(expandedWord)) continue;

        seen.add(expandedWord);
        addCandidate(words, displayWords, expandedWord);

        if (rule.continuationFlags.length > 0) {
          expandFromFlags(expandedWord, rule.continuationFlags, depth + 1, seen);
        }
      }
    }
  }

  for (const line of entries) {
    const { word, flags } = parseDictionaryEntry(line);
    if (word !== word.toLowerCase()) continue;

    addCandidate(words, displayWords, word);
    expandFromFlags(word, flags);
  }

  return {
    words: [...words].sort((a, b) => a.length - b.length || a.localeCompare(b)),
    displayWords,
  };
}

function toSqlValue(value) {
  return `'${String(value).replaceAll("'", "''")}'`;
}

async function writeJson(words) {
  const payload = {
    source: SOURCE,
    license: LICENSE,
    generatedAt: new Date().toISOString(),
    normalization: 'uppercase, accents removed, ñ treated as n',
    minLength: MIN_LENGTH,
    maxLength: MAX_LENGTH,
    count: words.length,
    words,
  };

  await mkdir(path.dirname(PUBLIC_OUTPUT), { recursive: true });
  await writeFile(PUBLIC_OUTPUT, `${JSON.stringify(payload)}\n`);
  console.log(`Wrote ${words.length} words to ${path.relative(ROOT_DIR, PUBLIC_OUTPUT)}`);
}

async function writeSql(words, displayWords) {
  const batchSize = 1000;
  const lines = [
    '-- Generated by scripts/build-spanish-dictionary.mjs --sql',
    '-- Run sql/20260525-009_create_dictionary_words.sql before this seed.',
    'BEGIN;',
  ];

  for (let index = 0; index < words.length; index += batchSize) {
    const batch = words.slice(index, index + batchSize);
    lines.push(
      'INSERT INTO public.dictionary_words (normalized_word, display_word, word_length, source, is_allowed) VALUES',
      batch
        .map((word) =>
          [
            toSqlValue(word),
            toSqlValue(displayWords.get(word) ?? word.toLowerCase()),
            word.length,
            toSqlValue(SOURCE),
            'TRUE',
          ].join(', '),
        )
        .map((row) => `  (${row})`)
        .join(',\n') +
        '\nON CONFLICT (normalized_word) DO UPDATE SET\n' +
        '  display_word = EXCLUDED.display_word,\n' +
        '  word_length = EXCLUDED.word_length,\n' +
        '  source = EXCLUDED.source,\n' +
        '  is_allowed = EXCLUDED.is_allowed;',
    );
  }

  lines.push('COMMIT;', '');
  await mkdir(path.dirname(SQL_OUTPUT), { recursive: true });
  await writeFile(SQL_OUTPUT, lines.join('\n'));
  console.log(`Wrote SQL seed to ${path.relative(ROOT_DIR, SQL_OUTPUT)}`);
}

const { words, displayWords } = buildDictionary();
await writeJson(words);

if (process.argv.includes('--sql')) {
  await writeSql(words, displayWords);
}
